#include <stdio.h>
#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	char *m_s1;
	char *m_s2;

	m_s1 = s1;
	m_s2 = s2;
	while (*m_s1 && *m_s2)
	{
		if (*m_s1 != *m_s2)
			return (*m_s1 - *m_s2);
		m_s1++;
		m_s2++;
	}
	return (*m_s1 - *m_s2);
}
