int		ft_str_is_alpha(char *str)
{
	char *m_str;

	m_str = str;
	while (*m_str)
	{
		if (*m_str < 'A' || *m_str > 'z')
			return (0);
		if (*m_str > 'Z' && *m_str < 'a')
			return (0);
		m_str++;
	}
	return (1);
}
