char	*ft_strupcase(char *str)
{
	char *m_str;

	m_str = str;
	while (*m_str)
	{
		if (*m_str >= 'a' && *m_str <= 'z')
			*m_str -= 'a' - 'A';
		m_str++;
	}
	return (str);
}
